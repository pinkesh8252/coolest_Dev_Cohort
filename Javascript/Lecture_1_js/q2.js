/**
 * fizz buzz
 *  number -> 20
    1->20
    if number divisble by 3 -> fizz
    if number divisble by 5 -> buzz
    if number 3 & 5 both se divisble -> fizzBuzz
    other wise -> number
 *  */
 let target = 20;
 for (let num = 1; num <= target; num++) {
      if (num % 5 == 0 && num % 3 == 0) {
        console.log("Fizz buzz");
         break;
     }
     else if (num % 3==0) {
         console.log("fizz");
     } else if (num % 5==0) {
         console.log("buzz");
     } else {
         console.log(num);
     }
 }